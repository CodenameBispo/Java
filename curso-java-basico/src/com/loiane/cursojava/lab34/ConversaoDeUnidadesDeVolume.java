package com.loiane.cursojava.lab34;

public class ConversaoDeUnidadesDeVolume {
	
	public static double litroCentimetros3(double litro) {
		
		return litro*1000; 
	}	
	
	public static double metro3Litros(double metro) {
		
		return metro*1000;
	}
	
	public static double metro3Pes3(double metro) {
		
		return metro*35.32;
	}
	
	public static double galaoUSPolegadas3(double galaoUS) {
		
		return galaoUS*231;
	}
	
	public static double galaoUSLitros(double galaoUS) {
		
		return galaoUS*3.785;
	}
}
