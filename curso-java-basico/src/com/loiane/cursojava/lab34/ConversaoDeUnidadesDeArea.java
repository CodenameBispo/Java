package com.loiane.cursojava.lab34;

public class ConversaoDeUnidadesDeArea {
	
	public static double metroPes(double metro) {
		
		return metro*10.76; 
	}	
	
	public static double pe2Centimetro(double pe) {
		
		return pe*929;
	}
	
	public static double milha2Acres(double milha) {
		
		return milha*640;
	}
	
	public static double acrePes2(double acre) {
		
		return acre*43.560;
	}
}
